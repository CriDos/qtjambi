package com.trolltech.qt.gui;

public class QContentsMargins {
    public QContentsMargins(int left, int top, int right, int bottom) {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
    }            
    public int left;
    public int top;
    public int right;
    public int bottom;
}